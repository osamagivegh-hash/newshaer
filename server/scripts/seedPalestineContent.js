const run = async () => {
  const reset = process.argv.includes('--reset');
  try {
    await connectDB();
    console.log('✓ متصل بقاعدة البيانات');

    if (reset) {
      const del = await Palestine.deleteMany({});
      console.log(`🗑️  تم حذف ${del.deletedCount} عنصراً قديماً من قسم فلسطين`);
    }

    const result = await Palestine.insertMany(items);
    console.log(`✅ تمت إضافة ${result.length} عنصراً إلى قسم فلسطين`);
    console.log(`📊 إجمالي العناصر الآن: ${await Palestine.countDocuments()}`);

    process.exit(0);
  } catch (err) {
    console.error('❌ خطأ في التغذية:', err);
    process.exit(1);
  }
};

run();
